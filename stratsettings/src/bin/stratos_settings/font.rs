    let mut cursor_x = x;
    for ch in text.chars() {
        if ch == ' ' {
            cursor_x += 6;
            continue;
        }
        let glyph = FONT.iter().find(|(c, _)| *c == ch);
        if let Some((_, cols)) = glyph {
            // Column-major 5×7: each of the first five bytes is one column; bit i is row i (LSB = top).
            for col_idx in 0..5 {
                let col = cols[col_idx];
                for row_idx in 0..7 {
                    if (col >> row_idx) & 1 == 1 {
                        let px = cursor_x + col_idx as i32;
                        let py = y + row_idx as i32;
                        if px >= 0 && px < w && py >= 0 && py < h {
                            let off = (py as u32 * stride + px as u32 * 4) as usize;
                            if off + 4 <= buf.len() {
                                buf[off..off + 4].copy_from_slice(&bytes);
                            }
                        }
                    }
                }
            }
        }
        cursor_x += 6;
    }
