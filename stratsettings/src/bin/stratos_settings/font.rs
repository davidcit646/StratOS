pub fn fill_rect(buf: &mut [u8], stride: u32, w: i32, h: i32, x: i32, y: i32, rw: i32, rh: i32, color: u32) {
    let b = color.to_le_bytes();
    let x0 = x.max(0);
    let y0 = y.max(0);
    let x1 = (x + rw).min(w);
    let y1 = (y + rh).min(h);
    for py in y0..y1 {
        for px in x0..x1 {
            let off = (py as u32 * stride + px as u32 * 4) as usize;
            if off + 4 <= buf.len() {
                buf[off..off + 4].copy_from_slice(&b);
            }
        }
    }
}
