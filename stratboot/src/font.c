EFI_STATUS strat_font_draw_char(
    StratGop *gop,
    INT32 x,
    INT32 y,
    CHAR8 ch,
    UINT8 r,
    UINT8 g,
    UINT8 b
) {
    if (gop == NULL || gop->gop == NULL) {
        return EFI_INVALID_PARAMETER;
    }

    UINT8 uch = (UINT8)ch;
    if (uch < 32 || uch > 127) {
        ch = '?';
    }

    UINTN idx = (UINTN)(ch - 32);
    for (UINTN row = 0; row < STRAT_FONT_HEIGHT; row++) {
        UINT8 bits = kFont8x8[idx][row];
        for (UINTN col = 0; col < STRAT_FONT_WIDTH; col++) {
            if (bits & (1 << col)) {
                strat_gop_put_pixel(gop, x + (INT32)col, y + (INT32)row, r, g, b);
            }
        }
    }

    return EFI_SUCCESS;
}

EFI_STATUS strat_font_draw_text(
    StratGop *gop,
    INT32 x,
    INT32 y,
    const CHAR8 *text,
    UINT8 r,
    UINT8 g,
    UINT8 b
) {
    if (gop == NULL || gop->gop == NULL || text == NULL) {
        return EFI_INVALID_PARAMETER;
    }

    INT32 cursor_x = x;
    INT32 cursor_y = y;

    for (UINTN i = 0; text[i] != '\0'; i++) {
        CHAR8 ch = text[i];

        if (ch == '\r') {
            cursor_x = x;
            continue;
        }
        if (ch == '\n') {
            cursor_x = x;
            cursor_y += STRAT_FONT_HEIGHT;
            if (cursor_y + STRAT_FONT_HEIGHT > (INT32)gop->height) {
                break;
            }
            continue;
        }

        if (cursor_y + STRAT_FONT_HEIGHT > (INT32)gop->height) {
            break;
        }

        if (cursor_x + STRAT_FONT_WIDTH > (INT32)gop->width) {
            // Truncate at right edge for now.
            break;
        }

        strat_font_draw_char(gop, cursor_x, cursor_y, ch, r, g, b);
        cursor_x += STRAT_FONT_WIDTH;
    }

    return EFI_SUCCESS;
}
